    private boolean kd_barangTerdaftar(String kd_barang) {
        String sql = "SELECT kd_barang FROM tb_sparepart WHERE kd_barang = ?";
        try (PreparedStatement stat = conn.prepareStatement(sql)) {
            stat.setString(1, kd_barang);
            ResultSet hasil = stat.executeQuery();
            return hasil.next();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Gagal memeriksa Kode Barang: " + e.getMessage(), "Kesalahan", JOptionPane.ERROR_MESSAGE);
            return false;
        }
    }

