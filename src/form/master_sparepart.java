    private void bhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bhapusActionPerformed
        String kdBarang = kd_barang.getText().trim();
        if (kdBarang.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Kode Barang belum diisi.", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (!kd_barangTerdaftar(kdBarang)) {
            JOptionPane.showMessageDialog(this, "Kode Barang tidak ditemukan.", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        int ok = JOptionPane.showConfirmDialog(null, "Apakah benar ingin dihapus?", "Peringatan!", JOptionPane.YES_NO_OPTION);
        if (ok == 0) {
            String sql = "delete from tb_sparepart where kd_barang='" + kdBarang + "'";
            try {
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
                kosong();
                aktif();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Data gagal dihapus" + e);
            }
            datatable();
        }
    }//GEN-LAST:event_bhapusActionPerformed

    private void bubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bubahActionPerformed
        String kdBarangText = kd_barang.getText().trim(); // Trim untuk menghapus spasi di awal dan akhir
        String namaBarangText = nama_barang.getText().trim();
        String hargaText = harga_barang.getText().trim();
        String jenisBarang = jenis_barang.getSelectedItem().toString();
        String merkBarang = merk_barang.getSelectedItem().toString();
        if (kdBarangText.isEmpty() || namaBarangText.isEmpty() || hargaText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua kolom harus diisi.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (kdBarangText.length() > 10) {
            JOptionPane.showMessageDialog(this, "Kode Barang harus terdiri dari 10 digit.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (jenisBarang.equals("Pilih Jenis Barang")) {
            JOptionPane.showMessageDialog(this, "Jenis barang harus dipilih.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (merkBarang.equals("Pilih Merek Barang")) {
            JOptionPane.showMessageDialog(this, "Merek barang harus dipilih.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            return;
        }
        String sql = "UPDATE tb_sparepart SET nama_barang=?, harga_barang=?, jenis_barang=?, merk_barang=? WHERE kd_barang=?"; // Perhatikan penggunaan UPDATE
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, namaBarangText);
            stat.setString(2, hargaText);
            stat.setString(3, jenisBarang);
            stat.setString(4, merkBarang);
            stat.setString(5, kdBarangText); // WHERE clause
            int rowsUpdated = stat.executeUpdate();
            if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(null, "Data berhasil diubah!");
                kosong();
                aktif();
            } else {
                JOptionPane.showMessageDialog(null, "Data gagal diubah. Kode Barang tidak ditemukan.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data gagal diubah!" + e, "Kesalahan", JOptionPane.ERROR_MESSAGE);
        }
        datatable();
    }//GEN-LAST:event_bubahActionPerformed

